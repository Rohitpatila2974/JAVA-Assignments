package DAY02;

public class uppercase_lowercase 
{
	public static void main(String args[])
	{
		char r='A';
		
		if(r>=65 && r<=90)
		{
			System.out.println("UPPER CASE");
		}
		else if(r>=97 && r<=122)
		{
		
	     System.out.println("LOWER CASE");
		}
		
		else
		{
		System.out.println("SPECIAL CHARACTER");
	}
	}
}
